package com.bean;

public class Syllabus {
	private int sem;
	private String link;
	public Syllabus(){
		
	}
	public void setSem(int sem) {
		this.sem=sem;
	}
	public void setLink(String link) {
		this.link=link;
	}
	public int getSem() {
		return sem;
	}
	public String getLink() {
		return link;
	}
}
